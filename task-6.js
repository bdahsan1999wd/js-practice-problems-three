// 1. Declare an array of 5 colors
let colors = ["red", "blue", "green", "yellow", "purple"];

// 2. Use join method to combine all elements into a string separated by commas
let colorString = colors.join(", ");

// 3. Print the result
console.log(colorString); // "red, blue, green, yellow, purple"

// 4. Print the original array to show it is not changed
console.log(colors);