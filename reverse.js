/* -------------------------while loop reverse way------------------------- */

var num = 10;
while(num >= 1 ){
    console.log('while loop reverse way...')
    console.log(num);
    num--;
}

/* -------------------------for loop reverse way---------------------------- */

for(var i = 10; i >= 1; i--){
    console.log('for loop reverse way')
    console.log(i);
}